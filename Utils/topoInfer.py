class RNJ:
    def __init__(self, S, D, T, delta=0.00001):
        self.S = S
        self.preD = D.copy()
        self.D = D
        self.T = np.array(T)
        self.tLength = T.shape[1]
        # U集合是S集合和D集合的并集
        self.U = self.S + self.D
        self.T_mean = np.mean(T, axis=1)
        std = np.square(np.std(T, axis=1, ddof=1))
        # 计算方差(s到i的路径长度)
        self.rho_i = {index: value for index, value in enumerate(std)}
        # 计算协方差(s到ij分开处的路径长度)
        self.rho_ij = self.cov_compute()
        self.rho_ij_pre = copy.deepcopy(self.rho_ij)
        self.E = []
        self.d_length = {}
        self.delta = delta

    def cov_compute(self):
        n = self.T.shape[0]
        cov = {}
        for i in self.preD:
            for j in self.preD:
                if i != j:
                    cov[(i, j)] = abs(
                        sum((self.T[i] - self.T_mean[i]) * (self.T[j] - self.T_mean[j])) / (self.tLength - 1))
                    cov[(j, i)] = cov[(i, j)]
        return cov

    def topoGenerate(self):
        f = 1
        i = 0
        j = 0
        while len(self.D) != 1:
            self.rho_ij[(i, j)] = 0
            self.rho_ij[(j, i)] = 0
            (i, j) = max(self.rho_ij, key=self.rho_ij.get)
            if i in self.D and j in self.D and i != j:
                f += 1
                while f in self.preD or f in self.S:
                    f += 1
                self.D.remove(j)
                self.D.remove(i)
                self.U.append(f)
                self.D.append(f)
                self.E.append((f, j))
                self.E.append((f, i))
                self.d_length[(f, i)] = self.rho_i[i] - self.rho_ij[(i, j)]
                self.d_length[(f, j)] = self.rho_i[j] - self.rho_ij[(i, j)]
                i_j_k = []

                for k in self.D:
                    if k != f and self.rho_ij[(i, j)] - self.rho_ij[(i, k)] <= self.delta / 2:
                        i_j_k.append(k)
                        self.D.remove(k)
                        self.E.append((f, k))
                        self.d_length[(f, k)] = self.rho_ij[(i, k)]
                for k in self.D:
                    if k != f:
                        size = len(i_j_k) + 2
                        # print(
                        #     f"self.rho_ij[(k, j)] is {self.rho_ij[(k, j)]}, self.rho_ij[(i, k)] is {self.rho_ij[(i, k)]}")
                        path_length = self.rho_ij[(k, j)] + self.rho_ij[(i, k)]
                        for node in i_j_k:
                            path_length += self.rho_ij[(k, node)]
                        self.rho_ij[(k, f)] = path_length / size
                        self.rho_ij[(f, k)] = self.rho_ij[(k, f)]
                        self.rho_i[f] = self.rho_ij[(i, j)]
        for k in self.D:
            self.E.append((self.S[0], k))
        return (self.U, self.E), self.d_length


def path_length(edges, d, shortest_path):
    delay = 0
    try:
        node = str(d)
        for i in range(len(shortest_path[node]) - 1):
            edge = (shortest_path[node][i], shortest_path[node][i + 1])
            if edge not in edges:
                edge = (shortest_path[node][i + 1], shortest_path[node][i])
            delay += edges[edge]
        return delay
    except nx.NetworkXNoPath:
        return 0


def generateTree(edges, s, tree=[], visited=[]):
    edges_2 = [item for item in edges if item[0] in s]
    visited.extend(s)
    tree.extend([item for item in edges_2 if item[1] not in visited])
    s = [item[1] for item in edges_2 if item[1] not in visited]
    if len(s) > 0:
        generateTree(edges, s, tree, visited)
    return tree


